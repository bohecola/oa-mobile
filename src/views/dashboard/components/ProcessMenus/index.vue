<template>
  <van-collapse v-model="activeNames" v-loading="loading">
    <van-collapse-item v-for="(menus, title) in menusMap" :key="title" :name="title" :border="false">
      <template #title>
        <div class="font-bold text-base">
          {{ title }}
        </div>
      </template>
      <div class="grid grid-cols-4 gap-x-6 gap-y-3">
        <div v-for="item in menus" :key="item.id" class="w-16 h-full flex flex-col items-center gap-2" @click="handleItemClick(item)">
          <div class="w-12 h-12 bg-[--van-primary-color] text-white text-xl text-center leading-[48px] rounded">
            {{ item.procdefName.slice(0, 1) }}
          </div>
          <div class="text-xs text-center">
            {{ item.procdefName }}
          </div>
        </div>
      </div>
    </van-collapse-item>
  </van-collapse>
</template>

<script setup lang='ts'>
import { getUseMap } from '@/api/workflow/task'

const { proxy } = getCurrentInstance() as ComponentInternalInstance

const activeNames = ref<string[]>([])
const loading = ref(false)
const menusMap = ref<Record<string, any>>({})

// 流程项点击
function handleItemClick(val: Record<string, any>) {
  proxy.$router.push({
    path: val.router,
    query: {
      type: 'add',
      nodeId: val.nodeId,
      procdefName: val.procdefName,
    },
  })
}

// 挂载
onMounted(async () => {
  loading.value = true
  const { data } = await getUseMap().finally(() => (loading.value = false))
  menusMap.value = data
  activeNames.value = Object.keys(data)
})
</script>
